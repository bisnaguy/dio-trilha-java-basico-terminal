public class Cadastro {
    private String nome;
    private int idade;

    public void setNome(String entNome){
        nome = entNome;
    }
    public String getNome(){
        return nome;
    }
    public void setIdade(int entIdade){
        idade = entIdade;
    }
    public int getIdade(){
        return idade;
    }
}
